		<p class="designtopbg clearfix">
			<strong><img src="images/photo/designtit04.gif" /></strong>
			<b class="designdown"><a href="#"><img src="images/photo/down01.jpg" /></a></b>
			<span class="dottline clorhl">
			根据装修工序， 泥瓦工程是装修中的面子工程，是装修完毕之后直接展示在人们眼前的效果，不像隐蔽工程是看不见的，所以业主们要是希望自己的家装修得精致漂亮的话，一定不能忽视泥瓦工程的施工环节。泥瓦工程的大概流程有以下几步：结构改造(没有要求则不改)、抹平水电施工沟、然后做地面防水、地面找平、再然后就是铺贴瓷砖。木工与泥瓦工程基本处于同一阶段，通常包括了木地板、木门窗、暖气罩、木窗帘盒、木墙裙、木护墙、木龙骨等施工。
			</span>
			<img src="images/photo/desiphoto19.jpg" class="mt20" />
			<p class="linedown">
				<b>  一、结构改造</b>
				1、对于“砖混”结构的建筑，凡是预制板墙一律不能拆除，也不能开门开窗，特别是厚度超过24厘米以上的砖墙，一般都属于承重墙，不能轻易拆除和改造。<br />
				2、门框是嵌在混凝土中的，不宜拆除，如果拆除或改造，就会破坏建筑结构，降低安全系数，重新安装门也比较困难。<br />
				3、阳台边的矮墙不能拆除或改变。一般房间与阳台之间的墙上都有一门一窗，这些门窗可以拆除，但窗以下的墙不能拆，因为这段墙是“配重墙”，它就像秤砣一样起着挑起阳台的作用，如果拆除这堵墙，就会使阳台的承重力下降，导致阳台下坠。<br />
				4、房间中的梁柱不能改。梁柱是用来支撑上层楼板的，拆除或改造就会造成上层楼板下掉，相当危险，所以梁柱绝不能拆除或改造。<br />
				5、墙体中的钢筋不能动。在埋设管线时，如将钢筋破坏，就会影响到墙体和楼板的承受力，留下安全隐患。
			</p>
		</p>
		<p class="linedown">
			<b> 二、防水及墙地面处理</b>
			1、在做防水处理的基层上刷一层改性沥青，铺一层玻璃丝布，然后再重复涂刷一次，最后在第二层玻璃丝布上涂刷一遍涂料，防水处理才算做完。防水涂料应涂刷均匀，玻璃丝布与防水层封闭严密，不能有漏刷、起鼓和脱落的现象，否则会影响防水效果。若选用聚氨酯做防水涂料，一般不用加玻璃丝布，只要反复涂刷2—3遍就可以了。<br />
			2、“蔽水”试验是检验室内防水质量的重要检验手段。在防水工程做完后，封好门口及下水口，在室内蓄满水达到一定液面高度，24小时内液面若无明显下降，即为合格。 “蔽水”试验完成后，在防水涂料层上再做一层水泥砂浆保护层，注意不要破坏已做好的防水涂料层。等水泥砂浆保护层干透后，就可以在上面铺贴墙地砖或喷刷涂料，进行正常的装饰装修了。<br />
			3、如果原来的腻子是防水腻子，就不用铲了，不平就需要刷一层界面剂后刮一遍腻子找平。如果不是，全部铲掉重新做。<br />
			4、腻子刮2-3遍就可以了。<br />
			5、在墙面开槽的地方，应该用石膏封上，贴上真正的牛皮纸（扯不断的），再刮腻子。有空鼓或者裂纹一定要进行处理。<br />
			6、水泥超过出厂期三个月就不能用了<br />
			7、不同品种、标号的水泥不能混用<br />
			8、黄砂一定要用河砂，用嘴尝味道就知道了
		</p>
		<p class="linedown">
			<b>三、瓷砖铺贴</b>
			1、基层处理时，应全部清理墙面上的各类污物，并提前一天浇水湿润，如基层为新墙时，待水泥砂浆七成干时凡应该进行排砖、弹线、粘贴墙面砖。<br />
			2、墙地砖要浸水两小时以上，阴干后才能贴<br />
			3、铺粘时遇到管线、灯具开关、卫生间设备的支承件等，必须用整砖套害吻合，禁止用非整砖拼凑粘贴。<br />
			4、墙地砖宁可多买几片，不要少买，否则容易出现色差<br />
			5、一面墙上不能有两排非整砖<br />
			6、擦缝完成后要立即对瓷砖进行清理<br />
			7、地面贴大理石(浅色)，石材背面要做防水<br />
			8、阳角处要割45度角<br />
			9、地砖要向地漏处倾斜，否则容易积水<br />
			10、墙砖碰到管道口要采用套割的形式，这样看起来还是整块的砖<br />
			11、地面大理石宜干铺<br />
			12、阳台地砖要注意排水方向<br />
			13、墙面瓷砖粘贴必须牢固,空鼓率在3%以内。无歪斜、缺棱掉角和裂缝等缺陷。<br />
			14、预留孔洞尺寸正确、边缘整齐。检查平整度误差小于2毫米,立面垂直误差小于2毫米，接缝高低偏差小于0.5毫米,平直度小于2毫米。
		</p>
