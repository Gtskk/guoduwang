					8、地板铺装完成后，先用刨子将表面刨平刨光，将地板表面清扫干净后涂刷地板漆，进行抛光上蜡处理。<br />
					9、所有木地板运到施工安装现场后，应拆包在室内存放一个星期以上，使木地板与居室温度、湿度相适应后才能使用。<br />
