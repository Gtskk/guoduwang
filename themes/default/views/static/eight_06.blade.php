				<span class="dottline">
					<em class="clorge">实木地板：</em>实木地板的花纹自然、脚感舒适、使用安全，是书房、卧室、客厅等地面装修的理想材料。但实木地板在森林覆盖率下降，大力倡导环保的今天，价格上也较其他地板贵一些。
				</span>
				<span class="dottline">
					<em class="clorge">强化复合地板：</em>强化复合地板是近几年来流行的地面材料。它克服了原木稳定性差的弱点，复合地板的强度高、规格统一、耐磨系数高、防腐、防蛀且装饰效果好，且打理方便，价格实惠；但相较于实木地板，脚感和环保方面略差。
				</span>
				<span class="dottline">
					<em class="clorge">实木复合地板：</em>不同树种的板材交错层压而成，克服了实木地板单向同性的缺点，干缩湿胀率较小，具有较好的尺寸稳定性，并保留了实木地板的自然木纹和舒适的脚感，价格优于实木地板，但环保性略差。
				</span>
				<span class="dottline">
					<em class="clorge">竹木地板：</em>竹子经过处理后制成的地板，既富有天然材质的自然美感，又有耐磨耐用的优点，而且防蛀、抗震。竹木地板冬暖夏凉，防潮耐磨。
				</span>
				<span>
					<em class="clorge">软木地板：</em>软木是华栎木的保护层，即树皮，俗称栓皮栎。软木的厚度一般为4-5厘米，优质的软木可达8-9厘米。与实木地板相比，软木地板在环保性能防潮性能上更出色。
