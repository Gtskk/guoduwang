		<p class="linedown clearfix">
				<b>● 橱柜选购攻略！</b>
				橱柜是厨房的主角，牵涉到水、电、煤气管管等线路，且订制时间较长，最好在家里装修做水电气改造之前把橱柜定制好，这样可以根据橱柜的位置和大小来设计各种线路的走向。那如何才能选购到优质的橱柜呢?橱柜是由门板，柜体，台面，五金组成的，消费者在挑选时就要在这四个部分看橱柜的质量。
				<b>一、门板</b>
				<span class="dottline">
					门板对橱柜的整体外观影响很大，因此消费者大都很注重门板是否漂亮，材质是否好。门板的材质有三聚氰胺双饰面，UV，烤漆，吸塑，实木等等种类，消费者可根据自己的喜好选择不同材质，但需要注意的一点是要选择环保材质，看门板材质是否达到E1级标准，只有达到这个标准门板才不会释放有害物质对人体产生危害。
				</span>
				<span class="fl" style="width:450px;">
					<b>二、柜体</b>
					<span class="dottline">国内橱柜的柜体以刨花板和中密度板为主流材质，两者相关不大，在购买时要选择正规品牌正规卖场，这样更有质量保证。
					</span>
					<b>三、台面</b>
					<span class="dottline">台面有天然石、人造石、防火板、不锈钢等，最近还出现玻璃台面的橱柜。消费者可以根据自己的经济能力和喜好选择适合的橱柜台面。天然石台面：具有天然的纹路，美观，质地坚硬耐磨，价格较低。但其硬度太高，受热或被撞时易断裂，并且很难修复，易滋生细菌，易渗油，易被染色。人造石台面：是占取市场较大份额的，人造石的可塑性很强，可以制作任何形状的台面，并且表面没有缝隙，不会渗入污渍，价格较天然石要高些。防火板：防火效果很好，耐油污和酸碱，但是容易被水侵蚀，导致脱胶、变形等。<br />
					不锈钢台面：实用性很强，但因其冷冰冰的面孔已退出家用橱柜的用材。
					</span>
					<b>四、五金</b>
					五金是最容易被消费者忽视的地方，其实五金对橱柜的使用效果有很大的影响，五金包括连接件、铰链和滑轨等。高档橱柜之所以好，一方面在于它的用材好，工艺性强，而它的收纳功能的齐全，全凭五金件的配置。“芬尼尔”、“海蒂诗”、“海富乐”等品牌是五金件中的高档品。
				</span>
